
numbers = [5, 2, 1, 7, 4]

# Append:
numbers.append(20)
print(numbers)

# Insert:
numbers.insert(1, 4)
print(numbers)

# Count Occurrence:
print(numbers.count(4))

# Remove:
numbers.remove(7)
print(numbers)

# Remove Last Item:
numbers.pop()
print(numbers)

# Sort:
numbers.sort()
print(numbers)

# Reverse:
numbers.reverse()
print(numbers)

# Check for Index:
print(50 in numbers)

# Remove All / Clear:
numbers.clear()
print(numbers)